"""
+ -> soma
- -> Subtração
* -> Multiplicação
/ -> Divisão (float)
// -> Divisão (int)
** -> Exponenciação
% -> Módulo (resto da divisão)
() -> altera a precedência
"""

print()

print('Soma:', 5 + 5)
print('Subtração:', 5 - 3)
print('Multiplicação:', 5 * 3)
print('Divisão (float):', 5 / 3)
print('Divisão (int): ', 10 // 3)
print('Divisão (int): ', 10 // 3.0)
print('Exponenciação:', 2 ** 3)
print('Precedência:', (3 + 1) * 2)

print()
